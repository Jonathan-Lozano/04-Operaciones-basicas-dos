public class Operacion {

	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return Este metodo resive dos variables como parametros del tipo int y
	 *         devuelve la suma de esos dos valores
	 */
	public int sumar(int num1, int num2) {
		return num1 + num2;
	}

	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return Este metodo resive dos variables como parametros del tipo int y
	 *         devuelve la resta de esos dos valores
	 */
	public int restar(int num1, int num2) {
		return num1 - num2;
	}

	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return Este metodo resive dos variables como parametros del tipo int y
	 *         devuelve la multiplicacion de esos dos valores
	 */
	public int multiplicar(int num1, int num2) {
		return num1 * num2;
	}

	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return Este metodo resive dos variables como parametros del tipo int y
	 *         devuelve la divicion de esos dos valores
	 */
	public int dividir(int num1, int num2) {
		return num1 / num2;
	}

}
